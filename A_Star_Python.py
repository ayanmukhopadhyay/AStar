from queue import PriorityQueue


def precompute_neighbors(grid):
    neighbors_dict = {}
    directions = [(0, -1), (0, 1), (-1, 0), (1, 0)]  # Left, Right, Up, Down
    for x in range(len(grid)):
        for y in range(len(grid[0])):
            if not grid[x][y]:
                neighbors = []
                for d in directions:
                    neighbor = (x + d[0], y + d[1])
                    if 0 <= neighbor[0] < len(grid) and 0 <= neighbor[1] < len(grid[0]) and not grid[neighbor[0]][neighbor[1]]:
                        neighbors.append(neighbor)
                neighbors_dict[(x, y)] = neighbors
    return neighbors_dict

def a_star_search(grid, start, goal, neighbors_dict):
    def heuristic(a, b):
        return abs(a[0] - b[0]) + abs(a[1] - b[1])

    start = tuple(start)
    goal = tuple(goal)
    frontier = PriorityQueue()
    frontier.put((0, start))
    came_from = {}
    cost_so_far = {}
    came_from[start] = None
    cost_so_far[start] = 0

    while not frontier.empty():
        current = frontier.get()[1]

        if current == goal:
            # Stop the search as the goal is reached
            break

        for next in neighbors_dict[current]:
            new_cost = cost_so_far[current] + 1
            if next not in cost_so_far or new_cost < cost_so_far[next]:
                cost_so_far[next] = new_cost
                priority = new_cost + heuristic(goal, next)
                frontier.put((priority, next))
                came_from[next] = current

    # Reconstruct path
    if goal not in came_from:
        return None, None  # If there's no path

    path = []
    current = goal
    while current != start:
        path.append(current)
        current = came_from[current]
    path.append(start)
    path.reverse()

    # Determine actions based on path
    actions = []
    for i in range(1, len(path)):
        prev, curr = path[i - 1], path[i]
        if curr[0] - prev[0] == 1: actions.append("Down")
        elif curr[0] - prev[0] == -1: actions.append("Up")
        elif curr[1] - prev[1] == 1: actions.append("Right")
        elif curr[1] - prev[1] == -1: actions.append("Left")

    return path, actions


